
public class User {
private int userId;
private String userName;

public User() {
	
}
public User(int uId, String uName) {
	userId = uId;
	userName = uName;
}
public int getUserId() {
	return userId;
}
public void setUserId(int uId) {
	userId = uId;
}
public String getUserName() {
	return userName;
}
public void setUserName(String uName) {
	userName = uName;
}


}
